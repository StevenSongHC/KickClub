package com.kc.model;

public class Province {
	
	private int id;				// 主键id
	private String name;		// 省份名称
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
