create table user(
	id bigint auto_increment primary key not null,
	u_email varchar(35) not null,
	u_password varchar(35) not null,
	u_name varchar(30) not null,
	u_photo varchar(50) default 'images/portrait/default.png',
	u_sex int(1) default 0,
	u_birth date,
	u_intro text,
	u_website varchar(150),
	u_interest varchar(256),
	u_from_province int(3),
	u_from_city int(5),
	u_present_province int(3),
	u_present_city int(5),
	u_senior varchar(50),
	u_college int(5),
	u_college_major varchar(50),
	u_join_college_date date,
	u_join_date date,
	u_last_login_date date,
	u_online int(1) default 1,
	u_account_status int(1) default 1
);